public class Node {
    private int frequency;
    private char value;
    public Node(char value) {
       this.value = value; 
    }
    
    public void setFrequency(int frequency) {
        this.frequency = frequency;
    }

    public char getValue() {
        return this.value;
    }
    
    public int getFrequency() {
        return this.value;
    }
}
